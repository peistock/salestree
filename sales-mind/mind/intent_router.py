def classify_intent(text: str) -> Tuple[str, float]:
    """
    意图分类：返回 (intent_type, confidence)
    intent_type: simple | complex | unknown
    """
    if not text:
        return ("unknown", 0.0)

    text = text.strip()

    # L1: 关键词匹配简单意图
    for category, patterns in _SIMPLE_INTENT_PATTERNS.items():
        for pattern in patterns:
            if re.search(pattern, text, re.IGNORECASE):
                return ("simple", 0.95)

    # L1: 复杂请求信号
    for pattern in _COMPLEX_SIGNALS:
        if re.search(pattern, text, re.IGNORECASE):
            return ("complex", 0.9)

    # 长度启发：超过 40 字默认复杂（兜底）
    if len(text) > 40:
        return ("complex", 0.6)

    # 兜底规则 1：有明确动作词 + 有具体内容目标 → simple（一轮工具即可）
    # 例："帮我查下茅台股价"、"看看今天新闻"、"搜一下北京天气"
    action_words = {"查", "搜", "找", "看", "问", "读", "查下", "查一查", "看看", "搜一下", "找一下", "问问"}
    has_action = any(w in text for w in action_words)
    if has_action and len(text) >= 8:
        return ("simple", 0.7)

    # 兜底规则 2：短文本默认 simple（避免误判复杂）
    if len(text) < 20:
        return ("simple", 0.5)

    # 兜底规则 3：长文本默认 complex
    return ("complex", 0.5)


def get_intent_label(text: str) -> str:
    """返回可读意图标签（用于日志和调试）"""
    intent, conf = classify_intent(text)
    return f"{intent}({conf:.0%})"
